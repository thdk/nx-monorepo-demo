/**
 * Meta-prompt that asks the model to draft an initial eval set for a skill.
 * Distilled from the upstream skill-creator guidance on what makes a good
 * eval query (concrete > abstract, near-miss negatives > obvious negatives).
 */

export interface InitPromptOptions {
  skillName: string;
  skillDescription: string;
  /** Full SKILL.md content, including frontmatter — gives the model concrete grounding. */
  skillContent: string;
  positiveCount: number;
  negativeCount: number;
  /** When true, ask the model to also draft 2-3 expectations per positive case. */
  withExpectations: boolean;
  expectationsPerPositive: number;
}

const INIT_SYSTEM_PROMPT = `You help bootstrap an eval set for a Claude Code skill.

skill-eval uses the eval set to verify two things:

1. TRIGGER EVALS — does the skill's frontmatter description correctly attract user queries that need it, and correctly NOT attract queries that don't?
2. OUTPUT EVALS (optional, per item) — when the skill is triggered, does the model produce a response that satisfies the expectations?

Your job is to draft realistic test queries based on the skill content provided.

QUALITY BAR FOR QUERIES — this is what separates a useful eval set from a useless one:

- Realistic. Concrete. Specific. Write what an actual developer would type — with file paths, scenario context, casual phrasing, sometimes lowercase or abbreviations. NOT abstract requests like "format this data" or "review my code".
- Bad: "Extract text from a PDF."
- Good: "ok so my boss just sent me Q4_sales_FINAL_v2.xlsx and she wants me to add a column with the profit margin as a percentage. revenue is col C, costs col D i think."

FOR POSITIVE CASES (should_trigger: true):
- Cover different aspects of what the skill does. Variety beats repetition.
- Mix formal and casual phrasings.
- Mix queries that name the skill's domain explicitly vs. queries that imply the need without naming it.
- Include at least one uncommon-but-valid use case.

FOR NEGATIVE CASES (should_trigger: false):
- Use NEAR-MISSES. Queries that share keywords or concepts with the skill but actually need something different.
- A naive keyword match would trigger them; the right answer is "no".
- Adjacent domain, ambiguous phrasing, or a context where another tool is more appropriate.
- AVOID obviously irrelevant queries — "write a fibonacci function" as a negative for a PDF skill is too easy. The negative cases should be genuinely tricky.

FOR EXPECTATIONS (when requested):
- Objective and evidence-checkable. A grader should be able to mark each pass/fail with confidence.
- Be specific. "Recommends X" or "Mentions the .route.tsx suffix" — not vague things like "the answer is correct".
- Include at least one negative expectation per item where natural ("does NOT recommend hyphenated single-file names").

Output ONLY a JSON object (no markdown fences, no prose before or after) with this shape:

{
  "skill_name": "<exact name from frontmatter>",
  "description": "Initial eval set generated by \`skill-eval init\`. Review and refine.",
  "evals": [
    {
      "id": 1,
      "name": "<short-kebab-case>",
      "query": "<realistic, concrete user query>",
      "should_trigger": true,
      "expectations": ["..."]
    },
    {
      "id": 2,
      "name": "<short-kebab-case>",
      "query": "<near-miss negative query>",
      "should_trigger": false
    }
  ]
}

Rules:
- skill_name MUST match the frontmatter exactly.
- ids start at 1 and increase by 1, positives first, then negatives.
- names are short kebab-case, descriptive of what the case tests.
- Omit \`expectations\` from negative cases.`;

export function buildInitPrompt(opts: InitPromptOptions): string {
  const {
    skillName,
    skillDescription,
    skillContent,
    positiveCount,
    negativeCount,
    withExpectations,
    expectationsPerPositive,
  } = opts;

  const expectationsClause = withExpectations
    ? `For each positive case, also draft ${expectationsPerPositive} expectation(s) — objective, evidence-checkable statements that a grader can pass/fail with confidence. See the expectations guidance in the system prompt.`
    : `Do NOT include the \`expectations\` field at all — this run is for trigger evals only.`;

  return [
    INIT_SYSTEM_PROMPT,
    '',
    '---',
    '',
    `# Skill metadata`,
    '',
    `- name: \`${skillName}\``,
    `- description: ${skillDescription}`,
    '',
    `# Full SKILL.md`,
    '',
    skillContent.trim(),
    '',
    '---',
    '',
    `# Task`,
    '',
    `Produce exactly ${positiveCount} positive (should_trigger: true) cases and exactly ${negativeCount} negative (should_trigger: false) cases.`,
    '',
    expectationsClause,
    '',
    `Return ONLY the JSON object.`,
  ].join('\n');
}
