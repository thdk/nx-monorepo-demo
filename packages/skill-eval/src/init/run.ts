import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

import type { EvalSet } from '../types.js';
import { runClaudeP, type RunClaudePOptions } from '../util/claude-p.js';
import { parseSkillMd } from '../util/parse-skill-md.js';
import { extractJsonObject } from '../output/grader-prompt.js';
import { evalSetSchema } from '../schemas.js';

import { buildInitPrompt } from './prompt.js';

export interface InitOptions {
  skillPath: string;
  /** Defaults to `<skillPath>/evals.json`. */
  outPath?: string;
  /** Overwrite an existing file. Defaults to false (errors instead). */
  force?: boolean;
  positiveCount?: number;
  negativeCount?: number;
  withExpectations?: boolean;
  expectationsPerPositive?: number;
  model?: string;
  claudeBin?: string;
  timeoutMs?: number;
  /** DI seam — replace the actual subprocess call. */
  runClaudeP?: (prompt: string, opts: RunClaudePOptions) => Promise<string>;
}

export interface InitResult {
  outPath: string;
  evalSet: EvalSet;
  durationMs: number;
}

const DEFAULT_MODEL = 'claude-sonnet-4-6';
const DEFAULT_TIMEOUT_MS = 5 * 60_000;

function findSchemaPath(): string | null {
  // Resolve relative to this compiled module: <pkg>/dist/init/run.js → <pkg>/schema/eval-set.schema.json
  try {
    const here = fileURLToPath(import.meta.url);
    const candidate = join(dirname(here), '..', '..', 'schema', 'eval-set.schema.json');
    if (existsSync(candidate)) return candidate;
  } catch {
    // import.meta.url may be unavailable in some test environments
  }
  return null;
}

function relativeSchemaPath(outPath: string): string | null {
  const schemaPath = findSchemaPath();
  if (!schemaPath) return null;
  const rel = relative(dirname(outPath), schemaPath);
  // Ensure a relative-looking path (not absolute) and POSIX separators for JSON portability.
  if (rel.startsWith('..') || rel.startsWith('./')) return rel.split('\\').join('/');
  return `./${rel.split('\\').join('/')}`;
}

export async function initEvalSet(options: InitOptions): Promise<InitResult> {
  const {
    skillPath,
    outPath: outPathOption,
    force = false,
    positiveCount = 8,
    negativeCount = 5,
    withExpectations = false,
    expectationsPerPositive = 3,
    model = DEFAULT_MODEL,
    claudeBin = 'claude',
    timeoutMs = DEFAULT_TIMEOUT_MS,
    runClaudeP: runner = runClaudeP,
  } = options;

  const outPath = outPathOption ?? join(skillPath, 'evals.json');

  if (existsSync(outPath) && !force) {
    throw new Error(
      `${outPath} already exists. Pass --force to overwrite, or remove the file and re-run.`,
    );
  }

  const skill = parseSkillMd(skillPath);
  const skillContent = readFileSync(join(skillPath, 'SKILL.md'), 'utf-8');

  const prompt = buildInitPrompt({
    skillName: skill.name,
    skillDescription: skill.description,
    skillContent,
    positiveCount,
    negativeCount,
    withExpectations,
    expectationsPerPositive,
  });

  const startedAt = Date.now();
  const responseText = await runner(prompt, { model, claudeBin, timeoutMs });
  const durationMs = Date.now() - startedAt;

  let raw: unknown;
  try {
    raw = extractJsonObject(responseText);
  } catch (err) {
    const message = err instanceof Error ? err.message : String(err);
    throw new Error(`init: model did not return a parsable JSON object: ${message}`);
  }

  // Validate via zod. We also coerce skill_name to match the actual skill, in
  // case the model echoed something slightly different.
  const evalSet = evalSetSchema.parse({
    ...(raw as Record<string, unknown>),
    skill_name: skill.name,
  });

  // Build the on-disk JSON object, prepending $schema if we can resolve it.
  const onDisk: Record<string, unknown> = {};
  const schemaRel = relativeSchemaPath(outPath);
  if (schemaRel) onDisk.$schema = schemaRel;
  onDisk.skill_name = evalSet.skill_name;
  onDisk.description =
    'description' in evalSet && typeof evalSet.description === 'string'
      ? evalSet.description
      : 'Initial eval set generated by `skill-eval init`. Review and refine.';
  onDisk.evals = evalSet.evals;

  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, JSON.stringify(onDisk, null, 2) + '\n');

  return { outPath, evalSet, durationMs };
}
